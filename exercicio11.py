#---------------- A LÓGICA BOOLEANA
# Este tipo de lógica é usado por TODOS os computadores (e, consequentemente, linguagens de progamação) porque só considera dois valores:

# TRUE (Verdadeiro)
# FALSA (Falso)

# Estes valores correspondem , respetivamente, ao 1 e ao 0- CÓDIGO BINÁRIO
# Os computadores funcionam todos mediante esta lógica (com zeros e uns - é a sua linguagem do computador)

#-----------LISTA DOS OPERADORES------------

